int	ft_isprint(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		while (str[i] > 31 && str[i] < 127)
		{
			i++;
		}
		if (str[i] != '\0')
		{
			return (0);
		}
	}
	return (1);
}
